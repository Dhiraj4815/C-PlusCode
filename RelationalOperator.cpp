#include <iostream>
using namespace std;
int main()
{

    int a = 10;
    int b = 5;
    cout << (a == b) << endl; // false=0
    cout << (a != b) << endl; // true=1
    cout << (a > b) << endl;  // true
    cout << (a < b) << endl;  // false
    cout << (a >= b) << endl; // true
    cout << (a <= b) << endl; // false

    return 0;
}